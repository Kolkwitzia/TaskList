    private void AddTaskButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddTaskButtonActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(TaskNumberTextField.getText());
        String name = TaskNameTextField.getText();
        Date dueDate = DueDateChooser.getDate();
        Task task = new Task(id, name, dueDate);
        task.tasks.add(task);
        
        Task tk = Task.CheckId(id);
        if (tk == null)
        {
            JOptionPane.showMessageDialog(rootPane, "ID number is already assigned to another task. Please enter a new ID");
        }
        else if (name.isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane, "Task can't be added without a name. Please add a name");
        }
        else
        {
            addFunction();
            close();
        }
        
        
        
        
    }//GEN-LAST:event_AddTaskButtonActionPerformed

