    public void addFunction(){
        try{
            String insert = "INSERT INTO Task (taskNumber, taskName, dueDate) VALUES (?,?,?)";
            
            pst = con.prepareStatement(insert);
            pst.setString(1, TaskNumberTextField.getText());
            pst.setString(2, TaskNameTextField.getText());
            pst.setString(3, DueDateChooser.getDateFormatString());
            
            pst.execute();
            
            JOptionPane.showMessageDialog(null, "Task added successfully");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

